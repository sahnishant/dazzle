#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const settingsDir = path.join(repoRoot, 'src', 'content', 'settings');
const contactSource = path.join(settingsDir, 'business-contact.json');
const contactOutput = path.join(repoRoot, 'src', 'data', 'businessContact.js');
const siteConfigSource = path.join(settingsDir, 'site-config.json');
const siteConfigOutput = path.join(repoRoot, 'src', 'data', 'siteConfig.js');

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, 'utf8'));
  } catch (error) {
    throw new Error(`${path.relative(repoRoot, filePath)}: invalid JSON (${error.message})`);
  }
}

function validateContact(contact) {
  assert(contact && typeof contact === 'object', 'business-contact.json must contain an object.');
  ['telHref', 'directMessageNumber', 'displayPhone', 'email', 'address', 'businessHours'].forEach((field) => {
    assert(typeof contact[field] === 'string', `business-contact.json: '${field}' must be a string.`);
  });
  assert(/^[+]\d{8,15}$/.test(contact.telHref), "business-contact.json: telHref must look like '+919999999999'.");
  assert(/^\d{8,15}$/.test(contact.directMessageNumber), 'business-contact.json: directMessageNumber must contain digits only.');
  assert(Array.isArray(contact.socials), 'business-contact.json: socials must be an array.');
  contact.socials.forEach((social, index) => {
    assert(social && typeof social === 'object', `business-contact.json: socials[${index}] must be an object.`);
    assert(typeof social.label === 'string' && social.label.trim(), `business-contact.json: socials[${index}].label is required.`);
    assert(typeof social.href === 'string' && social.href.trim(), `business-contact.json: socials[${index}].href is required.`);
  });
}

function validateSiteConfig(siteConfig) {
  assert(siteConfig && typeof siteConfig === 'object', 'site-config.json must contain an object.');
  ['name', 'url'].forEach((field) => {
    assert(typeof siteConfig[field] === 'string' && siteConfig[field].trim(), `site-config.json: '${field}' is required.`);
  });

  try {
    const parsedUrl = new URL(siteConfig.url);
    assert(['http:', 'https:'].includes(parsedUrl.protocol), 'site-config.json: url must use http or https.');
  } catch {
    throw new Error('site-config.json: url must be a valid absolute URL.');
  }
}

function writeContact(contact) {
  const output = `// src/data/businessContact.js
// AUTO-GENERATED by scripts/sync-settings.mjs.
// Edit src/content/settings/business-contact.json, then run npm run sync:settings.
export const businessContact = ${JSON.stringify(contact, null, 2)};
`;

  fs.writeFileSync(contactOutput, output, 'utf8');
  console.log(`Synced business contact to ${path.relative(repoRoot, contactOutput).replace(/\\/g, '/')}.`);
}

function writeSiteConfig(siteConfig) {
  const output = `// src/data/siteConfig.js
// AUTO-GENERATED by scripts/sync-settings.mjs.
// Edit src/content/settings/site-config.json, then run npm run sync:settings.
export const siteConfig = ${JSON.stringify(siteConfig, null, 2)};
`;

  fs.writeFileSync(siteConfigOutput, output, 'utf8');
  console.log(`Synced site config to ${path.relative(repoRoot, siteConfigOutput).replace(/\\/g, '/')}.`);
}

function main() {
  assert(fs.existsSync(contactSource), 'src/content/settings/business-contact.json does not exist.');
  assert(fs.existsSync(siteConfigSource), 'src/content/settings/site-config.json does not exist.');

  const contact = readJson(contactSource);
  validateContact(contact);
  writeContact(contact);

  const siteConfig = readJson(siteConfigSource);
  validateSiteConfig(siteConfig);
  writeSiteConfig(siteConfig);
}

try {
  main();
} catch (error) {
  console.error(`Settings sync failed: ${error.message}`);
  process.exit(1);
}
